export function getGrammarTopicMeta(tenseId) {
  if (!tenseId) return null
  return GRAMMAR_TOPIC_META[tenseId] || null
}

/** 写入课时 import 的轻量标记；关闭 GRAMMAR_LEARNING_UI_ENABLED 时恒为 null */
export function buildGrammarLessonPackMeta(tenseId) {
  if (!GRAMMAR_LEARNING_UI_ENABLED || !tenseId) return null
  if (!GRAMMAR_TOPIC_META[tenseId]) return null
  return { source: 'grammar', tenseId }
}
